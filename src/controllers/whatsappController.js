// Fun√ß√£o para transcrever o √°udio
async function transcribeAudio(audioUrl) {
    const audio = {
        uri: audioUrl, // URL do √°udio enviado pelo WhatsApp
    };
    const config = {
        encoding: 'OGG_OPUS', // Formato do √°udio que o WhatsApp utiliza para mensagens de voz
        sampleRateHertz: 16000,
        languageCode: 'pt-BR', // Definindo para portugu√™s do Brasil
    };
    const request = {
        audio: audio,
        config: config,
    };

    const [response] = await client.recognize(request);
    const transcription = response.results
        .map(result => result.alternatives[0].transcript)
        .join('\n');
    return transcription;
}



// Fun√ß√£o para gerar QR Code com op√ß√µes de configura√ß√£o
async function generateQRCode(text) {
    try {
        // Gera o QR code em formato ASCII com configura√ß√µes ajustadas
        const qrCodeASCII = await QRCode.toString(text, {
            type: 'terminal',
            errorCorrectionLevel: 'L', // N√≠vel de corre√ß√£o de erro baixo
            scale: 1 // Ajuste a escala para reduzir o tamanho
        });
        return qrCodeASCII;
    } catch (error) {
        throw new Error('Erro ao gerar o QR Code: ' + error.message);
    }
}

